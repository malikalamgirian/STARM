    /**
     * Removes stop words from POS tagged string
     * 
     * @param textToRemoveStopWordsFrom text to remove stop words from 
     * @param posTagSeparatorChar char(s) with which token and POS tag have been separated
     * @return the POS tagged string
     */
    public static String removeStopWords(String textToRemoveStopWordsFrom,
            String posTagSeparatorChar) {
        String stopWordsRemovedStringToReturn = "";
        
        String wordToStop, posTagOfWordToStop;

        try {
            String[] tokens = textToRemoveStopWordsFrom.split(" ");
            
            for (String token : tokens) {

                wordToStop = token.substring(0, token.indexOf(posTagSeparatorChar));
                posTagOfWordToStop = token.substring(token.indexOf(posTagSeparatorChar) + 1, token.length());

                if (!isStopWord(wordToStop)) {
                    stopWordsRemovedStringToReturn += wordToStop + posTagSeparatorChar + posTagOfWordToStop + " ";
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            System.err.println("Could not remove stop words from : " + textToRemoveStopWordsFrom);
            return textToRemoveStopWordsFrom;
        }

        return stopWordsRemovedStringToReturn.trim();
    }

    /**
     * Tests whether the word is a stop word or not
     * 
     * @param word the word to be checked if it is stop word or not
     * @return true if word is a stop word, false if the word is not a stop word
     */
    public static boolean isStopWord(String word) {
        boolean isStopWord = false;
        
        if (word != null) {
            for (int i = 0; i < stopWordsList.length; i++) {
                if (word.equalsIgnoreCase(stopWordsList[i])) {
                    return true;
                }
            }
        }

        return isStopWord;
    }

